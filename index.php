        <div id="description">
        <!-- DESCRIPTION -->
        	<strong>Bitcloud</strong> is an open source distributed database and escrow agent that allows publishers to pay nodes for storing and sharing encrypted data.<br><br>
                        The decentralized nature of <strong>Bitcloud</strong> allows anyone to publish large amounts of content that is free from censorship, high costs, and proprietary software.<br><br>
                        <strong>Bitcloud</strong> can be the base for decentralized applications that require data storage and bandwidth.<br><br>
                        There are many <strong>Bitcoin 2.0</strong> projects in the works right now, but they all still rely on some type of blockchain. In <strong>Bitcloud</strong>, the blockchain is replaced by a distributed database, also known as the nodepool.<br><br>
                        <strong>Bitcloud</strong> creates a new decentralized system of trust where entire web applications can be hosted without a centralized server.
